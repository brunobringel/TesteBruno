package br.com.fiap.exceptions;

public class IdNaoEncontradoException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public IdNaoEncontradoException() {
		super();
		
	}

	public IdNaoEncontradoException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public IdNaoEncontradoException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public IdNaoEncontradoException(String message) {
		super(message);
		
	}

	public IdNaoEncontradoException(Throwable cause) {
		super(cause);
	
	}

	
	
	
	
	
}
